namespace App\Repository;

use App\Entity\Order;
use App\Entity\User;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @method Order|null find($id, $lockMode = null, $lockVersion = null)
 * @method Order|null findOneBy(array $criteria, array $orderBy = null)
 * @method Order[]    findAll()
 * @method Order[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class OrderRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Order::class);
    }

    public function countMyOrders(User $user): ?int
    {
        return $this->createQueryBuilder('o')
            ->select('COUNT(o.id) as cnt')
            ->andWhere('o.user = :user')
            ->setParameter('user', $user)
            ->getQuery()
            ->getSingleScalarResult()
        ;
    }

    public function getRowJoinedAll(int $id)
    {
        return $this->createQueryBuilder('o')
            ->leftJoin('o.orderItems', 'orderItems')
            ->innerJoin('o.wineCard', 'wineCard')
            ->andWhere('o.id = :id')
            ->setParameter('id', $id)
            ->getQuery()
            ->getOneOrNullResult()
        ;
    }

    public function getJoinedAll(User $user)
    {
        return $this->createQueryBuilder('o')
            ->innerJoin('o.orderItems', 'orderItems')
            ->innerJoin('o.wineCard', 'wineCard')
            ->andWhere('o.user = :user')
            ->setParameter('user', $user)
            ->orderBy('o.id', 'DESC')
            ->getQuery()
            ->getResult()
        ;
    }


